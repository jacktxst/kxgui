Bitmap32 kxgui_font_atlas_bmp32(u32 bg, u32 fg) {
    u32 texture_width  = KXGUI_CHAR_W * KXGUI_FONT_COLS;
    u32 texture_height = KXGUI_CHAR_H * KXGUI_FONT_ROWS;
    u32 texture_size = texture_width * texture_height;
    Bitmap32 bmp =
    {
        .width  = texture_width,
        .height = texture_height,
        .pixels = malloc(texture_size * sizeof(u32))
    };
    for (int i = 0; i < texture_size; i++)
    {
        if (!KXGUI_FONT[i + (int)(i/texture_width)]) break;
        bmp.pixels[i] = KXGUI_FONT[i + (int)(i/texture_width)] == ' ' ? bg : fg;
    }
    return bmp;
}

